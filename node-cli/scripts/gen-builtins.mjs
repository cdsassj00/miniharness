// 내장 스킬(skills/*.md)·플러그인(plugins/*.mjs)·버전을 코드(src/builtins.js)로 임베드한다.
// 이렇게 해두면 npm 설치본이든 단일 실행파일(exe)이든 동일하게 동작한다(디스크 경로 의존 제거).
// 사용: node scripts/gen-builtins.mjs  (스킬/플러그인/버전이 바뀌면 다시 실행해 커밋)
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const pkg = JSON.parse(fs.readFileSync(path.join(root, "package.json"), "utf8"));

function parseFrontmatter(text) {
  const m = /^---\n([\s\S]*?)\n---\n?/.exec(text);
  if (!m) return { meta: {}, body: text.trim() };
  const meta = {};
  for (const line of m[1].split("\n")) {
    const i = line.indexOf(":");
    if (i > 0) meta[line.slice(0, i).trim()] = line.slice(i + 1).trim();
  }
  return { meta, body: text.slice(m[0].length).trim() };
}

// 스킬 수집
const skillsDir = path.join(root, "skills");
const skills = [];
for (const f of fs.existsSync(skillsDir) ? fs.readdirSync(skillsDir).sort() : []) {
  if (!f.endsWith(".md")) continue;
  const { meta, body } = parseFrontmatter(fs.readFileSync(path.join(skillsDir, f), "utf8"));
  skills.push({ name: f.replace(/\.md$/, ""), description: meta.description || "", body });
}

// 플러그인 파일 수집(정적 import 로 묶이게 한다)
const pluginsDir = path.join(root, "plugins");
const pluginFiles = (fs.existsSync(pluginsDir) ? fs.readdirSync(pluginsDir).sort() : []).filter((f) =>
  /\.(mjs|js)$/.test(f)
);

const importLines = pluginFiles.map((f, i) => `import p${i} from "../plugins/${f}";`).join("\n");
const pluginArr = pluginFiles.map((_, i) => `p${i}`).join(", ");

const out = `// AUTO-GENERATED by scripts/gen-builtins.mjs — 직접 수정하지 마세요.
// 스킬/플러그인/버전을 바꾼 뒤 \`node scripts/gen-builtins.mjs\` 로 재생성하세요.
${importLines}

export const VERSION = ${JSON.stringify(pkg.version)};

export const BUILTIN_PLUGINS = [${pluginArr}];

export const BUILTIN_SKILLS = ${JSON.stringify(skills, null, 2)};
`;

fs.writeFileSync(path.join(root, "src", "builtins.js"), out, "utf8");
console.log(`builtins 생성: 스킬 ${skills.length}개, 플러그인 ${pluginFiles.length}개, v${pkg.version}`);
